package classwork.chapter2;

public class ForSample {
    public static void main(String[] args) {
        int x;
        for (x = 0; x < 20; x = x + 1) {
            System.out.println("Elya");
            if (x == 10) {
                System.out.println("Elen");
            }
            if (x == 15) {
                System.out.println("Daniel");
            }
        }
    }
}
